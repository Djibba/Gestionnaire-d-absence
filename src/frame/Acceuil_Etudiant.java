    public void removeRowToTable(){
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        int rows = model.getRowCount();
        for(int i = rows - 1; i >=0; i--)
        {
            model.removeRow(i); 
        }
       
    }
     public void addRowToTable(){
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        ArrayList<Etudiant> list = se.RechercherParClasse(jComboBox_chooseClasse.getSelectedItem().toString());
        Object rowData[] = new Object[4];
        
         if (list.size()== 0) {
             //JOptionPane.showMessageDialog(null, "aucun etudiant trouvÃ©");
         }else{
        for (int i = 0; i < list.size(); i++) {                       
            rowData[0] = list.get(i).getNumeroCarte();
            rowData[1] = list.get(i).getNom();
            rowData[2] = list.get(i).getPrenom();
            rowData[3] = list.get(i).getEmailEtudiant();
            model.addRow(rowData);
        }
        }
    }
