    public void removeRowToTable(){
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        int rows = model.getRowCount();
        for(int i = rows - 1; i >=0; i--)
        {
            model.removeRow(i); 
        }
       
    }
    public void addRowToTable(){
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        Object rowData[] = new Object[4];
        try {
           String query="SELECT profil ,login , id_etudiant,id_enseignant FROM user " ;
           ResultSet result = requete.exeRead(query);
           String profil="";
           int id_etudiant = 0;
           String login="";
           int id_enseignant = 0;
           while(result.next()){
                profil= result.getString("profil");
                id_etudiant = result.getInt("id_etudiant");
                login= result.getString("login");
                id_enseignant = result.getInt("id_enseignant");
          //}
           if(profil.equals("Etudiant")){
               try {
                   String sql = "SELECT nom_etudiant,prenom_etudiant FROM etudiant WHERE etudiant.id_etudiant="+id_etudiant;
                   ResultSet res = requete.exeRead(sql);
                     while (res.next()) {
                         String nom = res.getString("nom_etudiant");
                         String prenom = res.getString("prenom_etudiant");
                         rowData[0]=nom;
                         rowData[1]=prenom;
                         rowData[2]=login;
                         rowData[3]=profil;//profil_etudiant;
                         model.addRow(rowData);
                     }
               } catch (Exception e) {
                     System.out.println(e.getMessage());
               }
           }
            else{
                 try {
                   String sql = "SELECT nom_enseignant,prenom_enseignant FROM enseignant WHERE enseignant.id_enseignant="+id_enseignant;
                   ResultSet res = requete.exeRead(sql);
                     while (res.next()) {
                         String nom = res.getString("nom_enseignant");
                         String prenom = res.getString("prenom_enseignant");
                         rowData[0]=nom;
                         rowData[1]=prenom;
                         rowData[2]=login;
                         rowData[3]=profil;
                         model.addRow(rowData);
                     }
               } catch (Exception e) {
                     System.out.println(e.getMessage());
               }
           }
           }
       } catch (SQLException e) {
           System.out.println(e.getMessage());
       }
    }
